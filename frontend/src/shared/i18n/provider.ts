export interface I18nMessages {
  [key: string]: string | I18nMessages;
}

export interface I18nConfig {
  locale: string;
  fallbackLocale: string;
  messages: Record<string, I18nMessages>;
}

export class I18nProvider {
  private config: I18nConfig;

  constructor(config: I18nConfig) {
    this.config = config;
  }

  t(key: string, params?: Record<string, string | number>): string {
    const defaultValue = params?.defaultValue as string | undefined;
    const cleanParams = { ...params };
    delete cleanParams.defaultValue; // Remove defaultValue from params
    
    const keys = key.split('.');
    let value: any = this.config.messages[this.config.locale] || this.config.messages[this.config.fallbackLocale];

    for (const k of keys) {
      if (value && typeof value === 'object' && k in value) {
        value = value[k];
      } else {
        // Fallback to fallback locale
        value = this.config.messages[this.config.fallbackLocale];
        for (const fallbackKey of keys) {
          if (value && typeof value === 'object' && fallbackKey in value) {
            value = value[fallbackKey];
          } else {
            return defaultValue || key; // Return defaultValue if provided, otherwise key
          }
        }
        break;
      }
    }

    if (typeof value !== 'string') {
      return defaultValue || key;
    }

    // Simple parameter replacement
    if (cleanParams) {
      return value.replace(/\{(\w+)\}/g, (match, paramKey) => {
        return cleanParams[paramKey]?.toString() || match;
      });
    }

    return value;
  }

  setLocale(locale: string): void {
    this.config.locale = locale;
  }

  getLocale(): string {
    return this.config.locale;
  }
}

